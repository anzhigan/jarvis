// ─── store ──────────────────────────────────────────────────────────────────
interface LangState {
  lang: Lang;
  setLang: (l: Lang) => void;
}

export const useLangStore = create<LangState>()(
  persist(
    (set) => ({
      lang: (navigator.language?.startsWith('ru') ? 'ru' : 'en') as Lang,
      setLang: (lang) => set({ lang }),
    }),
    { name: 'jarvis-lang' },
  ),
);

// ─── translator ──────────────────────────────────────────────────────────────
export function t(key: string, vars?: Record<string, string | number>): string {
  const lang = useLangStore.getState().lang;
  const dict = translations[lang] ?? translations.en;
  let v = dict[key] ?? translations.en[key] ?? key;
  if (vars) {
    for (const k in vars) v = v.replace(new RegExp(`\\{${k}\\}`, 'g'), String(vars[k]));
  }
  return v;
}

// Hook: subscribes to language changes, so components re-render when changed
export function useT() {
  const lang = useLangStore((s) => s.lang);
  return (key: string, vars?: Record<string, string | number>): string => {
    const dict = translations[lang] ?? translations.en;
    let v = dict[key] ?? translations.en[key] ?? key;
    if (vars) {
      for (const k in vars) v = v.replace(new RegExp(`\\{${k}\\}`, 'g'), String(vars[k]));
    }
    return v;
  };
}
